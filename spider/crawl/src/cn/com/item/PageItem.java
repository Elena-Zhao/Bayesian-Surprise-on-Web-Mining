package cn.com.item;

public class PageItem {
	
	
	private String url;
	private String fileName;
	private ResultListItem resultItem;

	
	
	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public ResultListItem getResultItem() {
		return resultItem;
	}

	public void setResultItem(ResultListItem resultItem) {
		this.resultItem = resultItem;
	}
	
	
	

}
